# Maximum of two numbers in Python

# Solution one : -

number_one = int(input("Enter a number : "))
number_two = int(input("Enter another number : "))




if number_one > number_two:
    print(f"{number_one}")

else:
    print(f"{number_two}")


# Solution Two : 
    
print(max(number_one,number_two))
    


